/**
 * Get a random user agent
 */
export function getRandomUserAgent(): string {
  const index = Math.floor(Math.random() * USER_AGENTS.length);
  return USER_AGENTS[index];
}

/**
 * Get a Chrome user agent (most common, best compatibility)
 */
export function getChromeUserAgent(): string {
  return USER_AGENTS[0]; // First Chrome on Windows
}

/**
 * Get user agent based on client type
 */
export function getUserAgentForClient(clientType: 'WEB' | 'ANDROID' | 'IOS'): string {
  switch (clientType) {
    case 'ANDROID':
      return 'Mozilla/5.0 (Linux; Android 13; SM-S918B) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Mobile Safari/537.36';
    case 'IOS':
      return 'Mozilla/5.0 (iPhone; CPU iPhone OS 17_2 like Mac OS X) AppleWebKit/605.1.15 (KHTML, like Gecko) Version/17.2 Mobile/15E148 Safari/604.1';
    case 'WEB':
    default:
      return getChromeUserAgent();
  }
}

